# Calcolare il perimetro di un triangolo dati i due cateti

# Variabili di Input
c1 = int(input("Digita il primo cateto: "))
c2 = int(input("Digita il secondo cateto: "))

# Formula da applicare
somma = (c1*c2)/2

print("Il perimetro di un triangolo rettangolo dato Cateto 1 =",c1,"e Cateto 2 =",c2,"vale:", somma)

